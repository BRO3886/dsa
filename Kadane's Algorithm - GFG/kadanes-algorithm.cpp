class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    int maxSubarraySum(int arr[], int n){
       int mCurr = arr[0], mGlob = arr[0];
       for(int i=1;i<n;i++){
           mCurr = max(arr[i],arr[i]+mCurr);
           if(mCurr>mGlob) mGlob = mCurr;
       }
       
       return mGlob;
    }
};

